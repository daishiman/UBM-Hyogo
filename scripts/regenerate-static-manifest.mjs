#!/usr/bin/env node
// scripts/regenerate-static-manifest.mjs
// Source: docs/00-getting-started-manual/specs/01-api-schema.md (canonical form schema)
// Output: apps/api/src/repository/_shared/generated/static-manifest.json
//
// 不変条件 #1: form schema は markdown を正本とし、コード側に schema を duplicate しない。
//             section title 等の補助メタは本スクリプトで静的に定義し、stableKey/kind は spec から抽出する。
// 決定論性: 同一入力 markdown に対して byte-identical な JSON を出力する。
//           Date.now() / Math.random() / 環境依存値は使わない。

import { readFile, writeFile, access } from "node:fs/promises";
import { createHash } from "node:crypto";
import { execFileSync } from "node:child_process";
import { dirname, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const __filename = fileURLToPath(import.meta.url);
const __dirname = dirname(__filename);
const REPO_ROOT = resolve(__dirname, "..");

const DEFAULT_SOURCE = "docs/00-getting-started-manual/specs/01-api-schema.md";
const DEFAULT_OUTPUT =
  "apps/api/src/repository/_shared/generated/static-manifest.json";

// Section key → 表示タイトル / position の静的マップ。
// 仕様 markdown の `## セクションN: <key>` は機械可読な英語 key を持つが日本語タイトルは
// 別レイヤの UI 表示メタなので、本スクリプトで canonical 化する。
const SECTION_META = {
  basic_profile: { title: "基本プロフィール", position: 1 },
  ubm_profile: { title: "UBMプロフィール", position: 2 },
  personal_profile: { title: "パーソナル", position: 3 },
  social_links: { title: "SNS / リンク", position: 4 },
  message: { title: "メッセージ", position: 5 },
  consent: { title: "同意事項", position: 6 },
  __system__: { title: "システム項目", position: 99 },
};

// section "consent" 配下の field は表示上 radio だが kind は "consent" として扱う
// (不変条件: consent は radio と区別される独立 kind)。
const CONSENT_KIND_OVERRIDE = new Set(["publicConsent", "rulesConsent"]);

// system fields (responseEmail) は markdown の `## system fields` 節から抽出。
const SYSTEM_FIELDS = {
  responseEmail: {
    sectionKey: "__system__",
    kind: "system",
    label: "回答者メールアドレス",
  },
};

function canonicalizeMarkdown(text) {
  // CRLF → LF、末尾改行 1 個に統一
  let t = text.replace(/\r\n/g, "\n");
  if (!t.endsWith("\n")) t += "\n";
  // 末尾連続改行を 1 個に丸める
  t = t.replace(/\n+$/g, "\n");
  return t;
}

function sha256Hex(buf) {
  return createHash("sha256").update(buf).digest("hex");
}

function parseSourceSpec(markdown) {
  const lines = markdown.split("\n");
  const sections = {}; // sectionKey → { fields: [{ stableKey, label, kind }] }

  let currentSection = null;
  const sectionHeaderRe = /^##\s*セクション\d+:\s*([a-z_]+)\s*$/;
  // table row: | `stableKey` | label | `kind` | required | visibility |
  const rowRe = /^\|\s*`([a-zA-Z0-9_]+)`\s*\|\s*([^|]+?)\s*\|\s*`([a-zA-Z]+)`\s*\|/;

  for (const line of lines) {
    const sh = line.match(sectionHeaderRe);
    if (sh) {
      currentSection = sh[1];
      if (!sections[currentSection]) sections[currentSection] = { fields: [] };
      continue;
    }
    if (!currentSection) continue;
    // Other ## headers reset section
    if (/^##\s/.test(line) && !sectionHeaderRe.test(line)) {
      currentSection = null;
      continue;
    }
    const m = line.match(rowRe);
    if (m) {
      const [, stableKey, labelRaw, kindRaw] = m;
      const label = labelRaw.trim();
      let kind = kindRaw.trim();
      if (currentSection === "consent" || CONSENT_KIND_OVERRIDE.has(stableKey)) {
        kind = "consent";
      }
      sections[currentSection].fields.push({ stableKey, label, kind });
    }
  }

  return sections;
}

function buildManifestObject({ parsed, sourceSpecHash, sourceSpecVersion, generatedAt }) {
  // sections: SECTION_META の position 昇順
  const sectionKeys = Object.keys(SECTION_META).sort(
    (a, b) => SECTION_META[a].position - SECTION_META[b].position,
  );
  const sections = sectionKeys.map((key) => ({
    key,
    title: SECTION_META[key].title,
    position: SECTION_META[key].position,
  }));

  // fields: parsed + system fields → stableKey 辞書順
  const fieldEntries = [];
  for (const sectionKey of Object.keys(parsed)) {
    if (!SECTION_META[sectionKey]) continue;
    for (const f of parsed[sectionKey].fields) {
      fieldEntries.push([
        f.stableKey,
        { sectionKey, kind: f.kind, label: f.label },
      ]);
    }
  }
  for (const [key, value] of Object.entries(SYSTEM_FIELDS)) {
    fieldEntries.push([key, value]);
  }
  fieldEntries.sort(([a], [b]) => (a < b ? -1 : a > b ? 1 : 0));
  const fields = Object.fromEntries(fieldEntries);

  // Top-level keys を固定順序で配置するため Object literal で明示的に列挙
  return {
    $comment:
      "AUTOGENERATED — do not edit by hand. Run `pnpm regenerate:static-manifest`. Stale detection: pnpm verify:static-manifest.",
    source: DEFAULT_SOURCE,
    sourceSpecVersion,
    sourceSpecHash,
    generatedAt,
    regenerateCommand: "pnpm regenerate:static-manifest",
    retirementCondition:
      "See docs/00-getting-started-manual/specs/01-api-schema.md § Static Manifest Retirement Condition",
    sections,
    fields,
  };
}

function tryGitMeta(absPath) {
  try {
    const out = execFileSync(
      "git",
      ["log", "-1", "--format=%h %cI", "--", absPath],
      { cwd: REPO_ROOT, stdio: ["ignore", "pipe", "ignore"] },
    )
      .toString()
      .trim();
    if (!out) return null;
    const [shortSha, iso] = out.split(/\s+/, 2);
    if (!shortSha || !iso) return null;
    return { sourceSpecVersion: shortSha, generatedAt: iso };
  } catch {
    return null;
  }
}

export async function regenerateStaticManifest(opts = {}) {
  const sourceSpecPath = opts.sourceSpecPath
    ? resolve(opts.sourceSpecPath)
    : resolve(REPO_ROOT, DEFAULT_SOURCE);
  const outputPath = opts.outputPath
    ? resolve(opts.outputPath)
    : resolve(REPO_ROOT, DEFAULT_OUTPUT);

  const raw = await readFile(sourceSpecPath, "utf8");
  const canonical = canonicalizeMarkdown(raw);
  const sourceSpecHash = `sha256:${sha256Hex(canonical)}`;

  const gitMeta = opts.gitMeta ?? tryGitMeta(sourceSpecPath);
  const sourceSpecVersion = gitMeta?.sourceSpecVersion ?? "deterministic";
  const generatedAt = gitMeta?.generatedAt ?? "deterministic";

  const parsed = parseSourceSpec(canonical);
  const manifest = buildManifestObject({
    parsed,
    sourceSpecHash,
    sourceSpecVersion,
    generatedAt,
  });

  const json = JSON.stringify(manifest, null, 2) + "\n";
  await writeFile(outputPath, json, "utf8");

  return { ok: true, manifestPath: outputPath, sourceSpecHash };
}

// CLI entry
if (
  import.meta.url === `file://${process.argv[1]}` ||
  process.argv[1]?.endsWith("regenerate-static-manifest.mjs")
) {
  try {
    const result = await regenerateStaticManifest();
    process.stdout.write(
      `[regenerate-static-manifest] OK ${result.manifestPath}\n`,
    );
    process.stdout.write(`  sourceSpecHash=${result.sourceSpecHash}\n`);
  } catch (e) {
    process.stderr.write(`[regenerate-static-manifest] FAIL ${e?.message ?? e}\n`);
    process.exit(1);
  }
}

// Re-export helpers for tests
export { canonicalizeMarkdown, sha256Hex, parseSourceSpec, buildManifestObject };

// expose access used in verify script reuse pattern
export const __access = access;
